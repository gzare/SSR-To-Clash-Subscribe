function GetContent(){
	try {
		if ( function_exists('file_get_contents')) {
			$_SubscribeContent = file_get_contents($_GET['url']);
		}elseif ( function_exists('curl_init')) {
			$_Curl = curl_init();
			curl_setopt($_Curl, CURLOPT_URL, $_GET['url']);
			curl_setopt($_Curl, CURLOPT_RETURNTRANSFER, 1);
			curl_setopt($_Curl, CURLOPT_HEADER, 0);
			$_SubscribeContent = curl_exec($_Curl);
			curl_close($_Curl);
		}else{
			die('Network Error');
		}

	}catch (Exception $e){
		die($e->getMessage());
	}
}
function CheckURL($url){
	if (function_exists('filter_var')){
		return (false !== filter_var($url, FILTER_VALIDATE_URL)) ? true : false;
	}else{
		$str="/^http(s?):\/\/(?:[A-za-z0-9-]+\.)+[A-za-z]{2,4}(?:[\/\?#][\/=\?%\-&~`@[\]\':+!\.#\w]*)?$/"; 
		if (!preg_match($str,$url)){  
        	return false;  
    	}else{  
    		return true;  
    	}  
	}
}
